<template>
  
  <div id="app" :class="typeof weather.main != 'undefined' && weather.main.temp > 27 ? 'warm' : ''">
    <main>
      <div class="search-box">
        <input type="text" name="" id="" class="search-bar" placeholder="City Name..." v-model="query" v-on:keypress="fetchWeather"/>
        

      </div>
      <div class="weather-wrap" v-if="typeof weather.main != 'undefined'">
        <div class="location-box">
          <div class="location">{{weather.name}}</div>
          <div class="date">{{ weather.sys.country }} </div>
        </div>

        <div class="weather-box">
          <div class="temp">{{weather.main.temp}}Â°C</div>
          <div class="weather-stat">{{weather.weather[0].main}}</div>
        </div>
      </div>
    </main>
  </div>
</template>

<script>


export default {
  name: 'App',
  data(){
    return {
      api_key: 'b1044c177bec5df2ba55fabe263d2e1b',
      url_base: 'https://api.openweathermap.org/data/2.5/',
      query: '',
      weather: {

      }

    }
  },
  methods:{
    fetchWeather(e){
      if (e.key == "Enter") {
        fetch(`${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`)
          .then(res => {
            return res.json();
          }).then(this.setResults);
    }
  }
  ,
  setResults(results){
    this.weather = results;
  }
}}
</script>

